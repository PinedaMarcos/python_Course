"""las funciones matematicas que tiene python
se encuentran en algo llamado librerias,
y estas se mandan a llamar con la palabra 
reservada import"""

#math es una libreria matematica
import math

#libreria de metodos aleatorios
import random

#eleva un numero a una potencia (pow)
print(math.pow(10,2))

#raiz cuadrada (sqrt)
print(math.sqrt(64))

#raiz cuadrada, en entero (isqrt)
print(math.isqrt(64))

# funcion seno (sin)
print(math.sin(90))

#funcion de coseno (cos)
print(math.cos(90))

#funcion de tangente (tan)
print(math.tan(90))

#factorial de un numero (factorial)
print(math.factorial(10))

#numeros al azar (randint)
print(random.randint(1, 100))