"""para comprobar si una cadena empieza con
alguna letra, solo sera cuestion de hacerlo
con el metodo startswith"""

cadena = ("Yo no decidi ser inge, solo tuve suerte")

print(cadena.startswith("Y"))


"""para comprobar si termina con una letra
usaremos el metodo endswith"""

print(cadena.endswith("e"))

